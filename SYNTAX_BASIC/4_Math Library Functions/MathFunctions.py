print(pow(10, 2))
print(pow(10, 1/3)) #can bac 3 cua 10
#ceil : lam tron len
print(math.ceil(10.5)) #11
#flooring : lam tron xuong
print(math.floor(10.6)) #10
#fractorial
print(math.factorial(1000))
#gcd(a, b)
print(math.gcd(10, 5))
#round(): phu thuoc vao phan thap phan
print(round(10.123))
#comb(n, k) : to hop chap k cua n 
print(math.comb(4, 2))
#perm: chinh hop
print(math.perm(4, 2))